// Show and hide site sections
function yesButton() {
  document.getElementById('yesChoice').style.display ='block'; // Displays "Yes" choice code block
  document.getElementById('noChoice').style.display ='none'; // Hides "No" choice code block
}

function noButton() {
  document.getElementById('noChoice').style.display = 'block'; // Displays "No" choice code block
  document.getElementById('yesChoice').style.display ='none'; // Hides "Yes" choice code block
}